function add(a, b){
    return a + b;
}

function subtract(a, b){
    return a - b;
}

function multiply(a, b){
    return a*b;
}

function divide(a, b){
    return a/b;
}

function increment(n){
    return (n + 1);
}

function decrement(n){
    return (n - 1);
}

function makeInt(string, n){
    return parseInt(string, 10);
}

function preserveDecimal(string, n){
    return parseFloat(string, 10);
}






























































































